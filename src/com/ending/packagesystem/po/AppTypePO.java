package com.ending.packagesystem.po;

/**
 * 应用类型表（AppType）
 * @author CodingEnding
 */
public class AppTypePO {
	private int id;
	private int type;
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
